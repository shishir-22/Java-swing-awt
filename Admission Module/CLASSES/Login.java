    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {
        String userName = txtUserName.getText();
        String passwd = new String(txtPassword.getPassword());
        if(DBManager.authenticateUser(userName, passwd)==true){
            String name = DBManager.getName(userName);
            String roleName = DBManager.getRole(userName);
            System.setProperty("name", name);       //JVM Level
            System.setProperty("role", roleName);
            //JOptionPane.showMessageDialog(this,"database working","successfull",JOptionPane.PLAIN_MESSAGE);
            MainFrame f = new MainFrame();
            f.setSize(900,600);
            f.setLocationRelativeTo(null);
            f.setExtendedState(JFrame.MAXIMIZED_BOTH);
            f.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "UserName/Password is Incorrect", "Error", JOptionPane.ERROR_MESSAGE);
            txtUserName.setText(null);
            txtPassword.setText(null);
            txtUserName.requestFocus();
        }
    }
